% #MxN  Specify the subplot division of the figure windows.
%
% Syntax with user-suplied subdivision
% =====================================
%
%     #MxN
%
% Syntax for automatic subdivision
% =================================
%
%     #auto
%
% Input arguments
% ================
%
% * `M` [ numeric ] - Number of rows of graphs in each figure window from
% this point on.
%
% * `N` [ numeric ] - Number of columns of graphs in each figure window
% from this point on.
%
% Description
% ============
%
% Example
% ========
%
% This command produces figure windows with 3x2 graphs in each.
%
%     #3x2
